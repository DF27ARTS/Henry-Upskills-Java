public class EjemploToLowerYUpperCase {
    public static void ejecutarEjemploToLowerYUpperCase() {
        String texto = "Hola Mundo";
        String resultadoEnMinusculas = texto.toLowerCase();
        System.out.println("El resultado en minusculas es : " + resultadoEnMinusculas);

        String resultadoEnMayusculas = texto.toUpperCase();
        System.out.println("El resultado en mayusculas es : " + resultadoEnMayusculas);
    }
}
